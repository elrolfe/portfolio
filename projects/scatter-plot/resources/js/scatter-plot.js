var chart = d3.select(".chart")
   .attr("width", width + margin.left + margin.right)
   .attr("height", height + margin.top + margin.bottom)
   .append("g")
   .attr("transform", "translate(" + margin.left + ", " + margin.top + ")");


chart.selectAll("circle")
   .data(FCC_DATA)
   .enter().append("circle")
   .attr("class", function(d) { return d.Doping == "" ? "clean" : "doping"; })
   .attr("cx", function(d) { return x(new Date(0, 0, 0, 0, 0, d.Seconds - dataAdjustment)); })
   .attr("cy", function(d) { return y(d.Place); })
   .attr("r", 5)
   .on("mouseover", function(d) {
      d3.select(".cyclist-name").text(d.Name);
      d3.select(".nationality").text(d.Nationality);
      d3.select(".year").text(d.Year);
      d3.select(".time").text(d.Time);
      if (d.Doping == "") {
         d3.select(".allegation").text("No doping allegations");
      } else {
         d3.select(".allegation").text(d.Doping);
      }
      d3.select(".tooltip")
         .style("opacity", 1)
         .style("top", (d3.event.pageY - 22) + "px")
         .style("left", (d3.event.pageX - 240) + "px")
   })
   .on("mouseout", function() {
      d3.select(".tooltip")
         .style("opacity", 0)
         .style("top", "-100px")
         .style("left", "-100px")
   });

